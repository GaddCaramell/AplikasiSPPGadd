    private void reftable(){
        DefaultTableModel md = (DefaultTableModel) datakls.getModel();
        
        md.getDataVector().removeAllElements();
        
        String sql = "select * from kelas";
        
        dbspp mdb = new dbspp();
        ResultSet rs = mdb.ambilData(sql);
        
        try {
            while(rs.next()){
                Vector row = new Vector();
                row.add(rs.getInt("id_kelas"));
                row.add(rs.getString("nama_kelas"));
                row.add(rs.getString("kompetensi_keahlian"));
                md.addRow(row);
            }
        } catch (SQLException e) {
            Logger.getLogger(Spp.class.getName()).log(Level.SEVERE,null, e);
        }
    }
