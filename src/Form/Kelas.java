    private void btnupdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnupdateMouseClicked
        // TODO add your handling code here:
        int nobaris = datakls.getSelectedRow();
        String kode = datakls.getValueAt(nobaris, 0).toString();
        String nmkls = nmklstxt.getText();
        String kk = kktxt.getText();
        String sql = "update kelas set nama_kelas='"+nmkls+"',kompetensi_keahlian='"+kk+"' where id_kelas='"+kode;

        dbspp mdb = new dbspp();
        if(mdb.ubahData(sql)){
            notif.setText("Data Tersimpan");
            reftable();
        }else{
            notif.setText("Gagal Tersimpan");
        }
        kosong();
    }//GEN-LAST:event_btnupdateMouseClicked

